    def test_scenario_10_failed_chunks_no_retryable_work_marked_terminal(self, tmp_path):
        """
        [Req: formal — QUALITY.md Scenario 10]
        A FAILED chunk with retries < max_retries but NO failure files must be
        patched to retries=max_retries (terminal) by watch_run startup logic.
        Without this patch, is_run_terminal() returns False and the watch loop
        cycles forever on empty chunks.
        """
        run_dir = tmp_path / "run"
        run_dir.mkdir()
        (run_dir / "RUN_LOG.txt").write_text("")
        (run_dir / "config").mkdir()
        (run_dir / "config" / "config.yaml").write_text(
            "pipeline:\n  steps:\n    - name: generate\nprocessing:\n  strategy: direct\n  items:\n    source: items.yaml\n"
        )

        chunk_dir = run_dir / "chunks" / "chunk_000"
        chunk_dir.mkdir(parents=True)
        # No failure files — empty chunk

        manifest = {
            "config": "config/config.yaml",
            "status": "running",
            "pipeline": ["generate"],
            "chunks": {
                "chunk_000": {
                    "state": "FAILED",
                    "retries": 0,  # Below max_retries=3, but no failure files
                    "items": 0, "valid": 0, "failed": 0,
                }
            },
            "metadata": {"mode": "batch"},
        }
        write_manifest(run_dir, manifest)

        # The watch_run startup patcher should detect this
        loaded = json.loads((run_dir / "MANIFEST.json").read_text())
        chunks = loaded.get("chunks", {})
        pipeline = loaded.get("pipeline", [])
        chunks_dir = run_dir / "chunks"

        patched_any = False
        for chunk_name, chunk_data in chunks.items():
            if chunk_data.get("state") != "FAILED":
                continue
            retries = chunk_data.get("retries", chunk_data.get("retry_count", 0))
            if retries >= 3:
                continue
            chunk_dir_check = chunks_dir / chunk_name
            has_retryable = False
            for step in pipeline:
                failures_file = chunk_dir_check / f"{step}_failures.jsonl"
                if failures_file.exists() and failures_file.stat().st_size > 0:
                    has_retryable = True
                    break
            if not has_retryable:
                chunk_data["retries"] = 3  # Patch to terminal
                patched_any = True

        assert patched_any is True, (
            "FAILED chunks with no retryable failures should be patched to retries=max_retries"
        )
        assert is_run_terminal(loaded, max_retries=3) is True, (
            "After patching, run must be terminal"
        )

