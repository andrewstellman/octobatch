def write_manifest(run_dir: Path, manifest: dict) -> None:
    """Write a MANIFEST.json to run_dir."""
    run_dir.mkdir(parents=True, exist_ok=True)
    (run_dir / "MANIFEST.json").write_text(json.dumps(manifest, indent=2))


def write_jsonl(path: Path, records: list) -> None:
    """Write records to a JSONL file, creating parent dirs as needed."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as f:
        for r in records:
            f.write(json.dumps(r) + "\n")


def make_run(run_dir: Path, pipeline: list[str], unit_ids: list[str],
             status: str = "complete") -> dict:
    """
    Build a minimal run with a single chunk where all units are validated.

    Returns the manifest dict.
    """
    run_dir.mkdir(parents=True, exist_ok=True)
    (run_dir / "RUN_LOG.txt").write_text("")
    chunk_dir = run_dir / "chunks" / "chunk_000"
    chunk_dir.mkdir(parents=True)

    # Write initial units
    units = [{"unit_id": uid} for uid in unit_ids]
    write_jsonl(chunk_dir / "units.jsonl", units)

    # Write validated records for each step
    for step in pipeline:
        write_jsonl(
            chunk_dir / f"{step}_validated.jsonl",
            [{"unit_id": uid, "step": step} for uid in unit_ids]
        )

    manifest = {
        "config": "config/config.yaml",
        "status": status,
        "pipeline": pipeline,
        "chunks": {
            "chunk_000": {
                "state": "VALIDATED",
                "items": len(unit_ids),
                "valid": len(unit_ids),
                "failed": 0,
                "retries": 0,
            }
        },
        "metadata": {},
    }
    write_manifest(run_dir, manifest)
    return manifest


def minimal_config(pipeline_steps: list | None = None) -> dict:
    """Create a minimal valid config dict for testing."""
    if pipeline_steps is None:
        pipeline_steps = [{"name": "generate"}]
    return {
        "pipeline": {"steps": pipeline_steps},
        "processing": {"strategy": "direct", "items": {"source": "items.yaml"}},
    }


